class CardapioCache {
  String documentId;
  String refeicao;
  String inicio;
  String dia;
  String data;
  String mes;
  String ano;
  String pratoPrincipal;
  String guarnicao;
  String dieta;
  String saladaCrua;
  String saladaCozida;
  String bebida;

  setDocumentId(docId) {
    this.documentId = docId;
  }

  setRefeicao(refeicao) {
    this.refeicao = refeicao;
  }

  setInicio(inicio) {
    this.inicio = inicio;
  }

  setDia(dia) {
    this.dia = dia;
  }

  setData(data) {
    this.data = data;
  }

  setMes(mes) {
    this.mes = mes;
  }

  setAno(ano) {
    this.ano = ano;
  }

  setPratoPrincipal(pratoPrincipal) {
    this.pratoPrincipal = pratoPrincipal;
  }

  setGuarnicao(guarnicao) {
    this.guarnicao = guarnicao;
  }

  setDieta(dieta) {
    this.dieta = dieta;
  }

  setSaladaCrua(saladaCrua) {
    this.saladaCrua = saladaCrua;
  }

  setSaladaCozida(saladaCozida) {
    this.saladaCozida = saladaCozida;
  }

  setBebida(bebida) {
    this.bebida = bebida;
  }
}
